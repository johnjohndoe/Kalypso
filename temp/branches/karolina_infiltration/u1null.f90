      subroutine u1null(feld,n)

!*  die subroutine u1null ist eine subroutine, die ein feld mit 4 byte
!*  grossen elementen auf null setzt.

!cx aend. bach 27.7.91 funktion analog zu u0null, da error-meldung des
!cx ms-fortrtan-compilers, wenn feldtyp in u0null nicht integer entspricht

      real feld(n)
      INTEGER, INTENT(IN) :: n

      i=0
100   i=i+1
      if (i .gt. n) return
      feld(i)=0.
      goto 100
end
