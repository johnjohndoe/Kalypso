	subroutine moveblank(char10)

CHARACTER(LEN=10)		:: char10
INTEGER					:: iblank
    
iblank = 0
imax=lastchar(char10)

do 100 i=1,imax
		if(char10(i:i).eq.' ') goto 100
		iblank=i-1
		goto 101
100	continue

101	do 102 i=1,imax-iblank
		char10(i:i)=char10(iblank+i:iblank+i)
102	continue

do 103 i=imax-iblank+1,imax
		char10(i:i)=' '
103	continue

return
end
