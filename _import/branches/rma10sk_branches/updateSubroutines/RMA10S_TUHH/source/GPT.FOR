
      SUBROUTINE GPT(SL,WAITPH)
      SAVE
      DOUBLE PRECISION A,B,XA,YA,XC,YC,WT,TOT,ZC
      DIMENSION SL(64,3),WAITPH(64)
      DIMENSION A(4),B(4),XA(4),YA(4),XC(64),YC(64),WT(64),ZC(64)
      DATA YA/0.861136311594053D00,0.339981043584856D00,
     1      -0.339981043584856D00,-0.861136311594053D00/
      DATA A/0.347854845137454D00,0.652145154862546D00,
     A       0.652145154862546D00,0.347854845137454D00/
      DATA XA/0.139759864343781D00,0.416409567631083D00,
     1        0.723156986361876D00,0.942895803885482D00/
      DATA B/0.0311809709500081D00,0.129847547608232D00,
     1       0.203464568010271D00,0.135506913431488D00/
      II=0
      TOT=0.
      DO 200 K=1,4
      DO 100 I=1,4
      DO 100 J=1,4
      II=II+1
      XC(II)=1.D00-XA(I)
      YC(II)=XA(I)*YA(J)
      ZC(II)=YA(K)
      WT(II)=B(I)*A(J)*A(K)/2.
      TOT=TOT+WT(II)
  100 CONTINUE
  200 CONTINUE
      DO 300 N=1,64
      SL(N,1)=XC(N)
      SL(N,2)=YC(N)
      SL(N,3)=ZC(N)
      WAITPH(N)=WT(N)
  300 CONTINUE
      RETURN
      END
