      SUBROUTINE SWITON(K,ISWT,IYRR,DAYOFY,TET)
	USE TSVAR
      INTEGER DAYOFY

	ISWT=1

      IF(K .EQ. 0) RETURN

	IF(IYRR .EQ. IYTIM) THEN
	  RTIME=(DAYOFY-1)*24.+TET
      ENDIF
	DO L=1,NTIMEMX

        IF(CTIMEON(K,L) .LT.  -9000) RETURN 
        IF(RTIME .GE. CTIMEON(K,L)) THEN
	    IF(RTIME .LT. CTIMEOF(K,L)) THEN
	      ISWT=0
	      RETURN
	    ENDIF
	  ELSE
	    RETURN
	  ENDIF
	ENDDO
	RETURN
	END