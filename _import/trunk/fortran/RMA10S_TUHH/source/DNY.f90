!
      REAL*8 FUNCTION DNY(IT,K,X,Y)
      SAVE
!-
      REAL*8 X,Y
!......FUNCTION TO DETERMINE Y-DERIVATIVE OF SHAPE FUNCTION
!-
      IF(IT == 1) GO TO 500
!-
!......TRIANGULAR ELEMENT
!-
      GO TO(110,120,130,140,150,160),K
  110 DNY=-3.D0+4.D0*X+4.D0*Y
      RETURN
  120 DNY=-4.D0*X
      RETURN
  130 DNY=0.D0
      RETURN
  140 DNY=4.D0*X
      RETURN
  150 DNY=4.D0*Y-1.D0
      RETURN
  160 DNY=4.D0-4.D0*X-8.D0*Y
      RETURN
!-
!......QUADRILATERAL ELEMENT
!-
  500 CONTINUE
      GO TO (510,520,530,540,550,560,570,580),K
  510 DNY=-(1.D0-X)*(-2.D0*Y-X)/4.D0
      RETURN
  520 DNY=-(1.D0-X*X)/2.D0
      RETURN
  530 DNY=-(1.D0+X)*(X-2.D0*Y)/4.D0
      RETURN
  540 DNY=-Y*(1.D0+X)
      RETURN
  550 DNY=(1.D0+X)*(2.D0*Y+X)/4.D0
      RETURN
  560 DNY=(1.D0-X*X)/2.D0
      RETURN
  570 DNY=(1.D0-X)*(2.D0*Y-X)/4.D0
      RETURN
  580 DNY=-Y*(1.D0-X)
      RETURN
      END
